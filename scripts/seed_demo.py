def main() -> None:
    # На SQLite (dev без докера) создаём таблицы; на Postgres схему ведёт Alembic.
    engine = get_engine()
    if engine.dialect.name == "sqlite":
        init_db(engine)

    with session_scope() as db:
        try:
            company, owner = accounts.register_company(
                db, company_name="Bronx Fitness", slug=SLUG,
                owner_email=OWNER_EMAIL, owner_password=OWNER_PASSWORD,
                owner_name="Ляззат Иргалиева")
            db.commit()
        except AccountError as e:
            print(f"⚠️  Компания «{SLUG}» уже существует ({e}).")
            print(f"    Вход владельца:  slug={SLUG}  email={OWNER_EMAIL}  пароль={OWNER_PASSWORD}")
            print( "    Чтобы пересоздать с нуля (dev): docker compose down -v && docker compose up --build")
            return

        # Сотрудник-администратор.
        try:
            emp = accounts.create_user(
                db, company.id, email=EMP_EMAIL, password=EMP_PASSWORD,
                role="employee", full_name="Айгерим")
            db.commit()
        except AccountError:
            emp = None

        # Документы (ingest_text сам считает эмбеддинги через get_embedder()).
        print("Загружаю и публикую регламенты (считаются эмбеддинги)…")
        for d in DOCS:
            doc = kb.ingest_text(
                db, company.id, title=d["title"], content=d["content"],
                category=d["category"], created_by=owner.id, publish=True)
            db.commit()
            print(f"  ✅ «{d['title'][:48]}…»")

        # Онбординг-курс «в один клик» из всех стандартов: публикуем и назначаем
        # сотруднику, чтобы микро-обучение было видно сразу при показе клиенту.
        print("Собираю курс онбординга и назначаю сотруднику…")
        track = tracks.autobuild_track(
            db, company.id, title="Онбординг новичка",
            description="Короткие карточки по стандартам клуба + мини-тесты.",
            created_by=owner.id)
        tracks.set_track_status(db, company.id, track.id, "published")
        if emp:
            tracks.enroll(db, company.id, track.id, emp.id)
        db.commit()
        print(f"  ✅ Курс «{track.title}» опубликован и назначен")

        # Командный центр: подключаем сеть точек и наполняем их отзывами (демо-
        # источник), затем сразу разбираем — тональность, жалобы, привязка к
        # стандартам. Разные профили → видно, какой филиал проседает.
        print("Подключаю сеть точек и собираю «Командный центр» (отзывы → инсайты)…")
        for p in POINTS:
            point = reviews_mod.connect_point(
                db, company.id, name=p["name"], url=p["url"])
            db.commit()
            result = reviews_mod.sync_and_analyze(
                db, company.id, point.id,
                source=FakeReviewSource(profile=p["profile"], prefix=p["prefix"]))
            print(f"  ✅ «{p['name']}»: загружено {result['added']}, "
                  f"разобрано {result['analyzed']}")

    print("\n" + "=" * 64)
    print("  ДЕМО-КОМПАНИЯ ГОТОВА — можно показывать клиенту")
    print("=" * 64)
    print(f"  Открой:          http://localhost:8000")
    print(f"  Компания (slug): {SLUG}")
    print(f"  Владелец:        {OWNER_EMAIL} / {OWNER_PASSWORD}")
    print(f"  Сотрудник:       {EMP_EMAIL} / {EMP_PASSWORD}")
    print("\n  Что показать боту (есть в стандартах):")
    print("   • «За сколько дней оформлять заморозку абонемента?»")
    print("   • «Можно ли вернуть деньги за абонемент и сколько удержат?»")
    print("   • «Как часто протирать тренажёры?»")
    print("  И вопрос-ловушка (бот честно откажет):")
    print("   • «Какая зарплата у тренера?»")
    print("\n  Микро-обучение:")
    print(f"   • Войди сотрудником ({EMP_EMAIL}) → раздел «Обучение» →")
    print("     пройди курс карточками со свайпом и мини-тестами.")
    print(f"   • Войди владельцем → «Обучение» → видно прогресс сотрудника.")
    print("\n  Командный центр (для владельца/управляющего):")
    print("   • Войди владельцем → «Командный центр».")
    print("   • Вкладка «Вся сеть» — сравнение филиалов: видно, что точка")
    print("     «Сатпаева» проседает, а «Достык» — лучшая.")
    print("   • Выбери точку — пульс, главные боли клиентов (жалобы,")
    print("     привязанные к стандартам) и лента отзывов.")
    print("   • Кнопка «Обновить отзывы» подтягивает и разбирает новые.")
    print("\n  Цифровой опер-дир (агент с инструментами):")
    print("   • Войди владельцем → «Опер-дир» и спроси свободным текстом:")
    print("     «Какая точка проседает и что делать?» — агент сам поднимет")
    print("     отзывы, точки и стандарты и соберёт ответ (нужен ключ LLM).")
    print("=" * 64)


if __name__ == "__main__":
    main()
